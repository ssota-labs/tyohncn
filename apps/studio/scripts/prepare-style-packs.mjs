#!/usr/bin/env node
/**
 * Prepare Studio style packs for Next/PostCSS compilation:
 * - Copy registry style-*.css into apps/studio/styles/packs/
 * - Rewrite *-vars packs to use distinct scope classes (.style-mira-vars)
 *   so they can coexist with apply packs in one CSS bundle.
 * StylePackLink is replaced by className switching on the catalog wrapper.
 */
import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const studioRoot = path.resolve(__dirname, "..")
const repoRoot = path.resolve(studioRoot, "../..")
const srcDir = path.join(repoRoot, "packages/registry/styles")
const outDir = path.join(studioRoot, "styles/packs")

fs.mkdirSync(outDir, { recursive: true })

const files = fs
  .readdirSync(srcDir)
  .filter((f) => f.startsWith("style-") && f.endsWith(".css"))
  .sort()

const imports = []

for (const file of files) {
  const name = file.replace(/^style-/, "").replace(/\.css$/, "")
  let css = fs.readFileSync(path.join(srcDir, file), "utf8")

  // Vars packs share scope with apply packs (e.g. mira-vars → .style-mira).
  // Studio needs a distinct class so both can live in one compiled bundle.
  if (name.endsWith("-vars")) {
    const base = name.replace(/-vars$/, "")
    const from = new RegExp(`\\.style-${base}\\b`, "g")
    const to = `.style-${name}`
    css = css.replace(from, to)
  }

  fs.writeFileSync(path.join(outDir, file), css)
  imports.push(`@import "./packs/${file}";`)
  console.log(`prepared ${file}`)
}

const index = `/* Auto-generated by scripts/prepare-style-packs.mjs — do not edit */
${imports.join("\n")}
`
fs.writeFileSync(path.join(studioRoot, "styles/packs.css"), index)

// Remove obsolete raw public copies / compiled attempts
for (const dir of ["public/registry-styles", "public/compiled-styles"]) {
  const p = path.join(studioRoot, dir)
  if (fs.existsSync(p)) {
    fs.rmSync(p, { recursive: true, force: true })
    console.log(`removed ${dir}`)
  }
}

console.log(`Wrote styles/packs.css (${files.length} packs)`)
